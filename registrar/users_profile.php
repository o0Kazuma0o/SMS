  <script>
    function showPopupMessage(message, type = 'success') {
      const popup = document.createElement('div');
      popup.className = `popup-message ${type}`;
      popup.innerText = message;
      document.body.appendChild(popup);

      popup.style.opacity = '1';
      setTimeout(() => {
        popup.style.opacity = '0';
        setTimeout(() => popup.remove(), 500);
      }, 3000);
    }

    // Show popup on load if a session message exists
    window.onload = function() {
      <?php if (isset($_SESSION['error_message'])): ?>
        showPopupMessage('<?= $_SESSION['error_message']; ?>', 'error');
        <?php unset($_SESSION['error_message']); ?>
      <?php elseif (isset($_SESSION['success_message'])): ?>
        showPopupMessage('<?= $_SESSION['success_message']; ?>', 'success');
        <?php unset($_SESSION['success_message']); ?>
      <?php endif; ?>
    };

    // Handle profile edit form submission
    document.getElementById('profileEditForm').addEventListener('submit', function(event) {
      event.preventDefault();
      const formData = new FormData(this);

      fetch('update_profile.php', {
          method: 'POST',
          body: formData
        })
        .then(response => response.json())
        .then(data => {
          if (data.success) {
            showPopupMessage(data.message, 'success');
          } else {
            showPopupMessage(data.message, 'error');
          }
        })
        .catch(error => {
          console.error('Error:', error);
          showPopupMessage('An unexpected error occurred.', 'error');
        });
    });

    // Handle change password form submission
    document.getElementById('changePasswordForm').addEventListener('submit', function(event) {
      event.preventDefault();
      const formData = new FormData(this);

      fetch('change_password.php', {
          method: 'POST',
          body: formData
        })
        .then(response => response.json())
        .then(data => {
          if (data.success) {
            showPopupMessage(data.message, 'success');
          } else {
            showPopupMessage(data.message, 'error');
          }
        })
        .catch(error => {
          console.error('Error:', error);
          showPopupMessage('An unexpected error occurred.', 'error');
        });
    });
  </script>



